#include <libft.h>

void	ft_append(char **str, char c)
{
	char	s[2];

	s[0] = c;
	s[1] = 0;
	if (str)
	{
		if (!(*str))
			*str = ft_strdup(s);
		else
			*str = ft_join_free(*str, s);
	}
}
