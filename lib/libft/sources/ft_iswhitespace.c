#include <libft.h>

int	ft_iswhitespace(char *s)
{
	if (!s)
		return (0);
	if (*s == '\n' || *s == ' ' || *s == '\t'
		|| *s == '\f' || *s == '\v' || *s == '\r')
		return (1);
	return (0);
}
